-- ============================================================================
-- Описание запроса:
-- Файл: `eav/q33_phone_numbers.sql`.
-- Стратегия: EAV (Entity-Attribute-Value).
-- Модель стратегии: данные процесса разделены на сущность процесса и набор переменных по путям/атрибутам.
-- Типовые таблицы стратегии: обычно `eav_process_events` + `eav_process_variables` (в текущем наборе также встречаются унифицированные представления).
-- Назначение данного запроса: получение детальной выборки для анализа.
--
-- Логика выполнения запроса:
-- 1) Выбор источника данных: eav_process_events.
-- 3) Применение фильтров WHERE для отбора релевантных строк.
-- 7) Ограничение объёма выдачи через LIMIT.
--
-- Ожидаемые возвращаемые данные и формат:
-- - Тип результата: детальный набор (строки исходного уровня).
-- - Формат строк: одна строка результата на запись/группу согласно SELECT.
-- - Порядок столбцов: соответствует порядку полей в SELECT.
-- - Столбцы результата:
--   - process_id: идентификатор (STRING/UUID/INTEGER по схеме источника).
--   - var_path: текст/структура (STRING/JSON/ARRAY).
--   - phone: текст/структура (STRING/JSON/ARRAY).
-- ============================================================================
SELECT 
    process_id,
    var_path,
    var_value as phone
FROM eav_process_variables
WHERE var_path LIKE '%phoneNumber%'
  AND var_type = 'string'
LIMIT 100
