-- ============================================================================
-- Стратегия: Default.
-- Выборка основных колонок и пример колонок переменных с точкой в имени.
-- Идентификаторы с точкой в Druid SQL задаются в двойных кавычках.
-- ============================================================================
SELECT
    id,
    process_id,
    process_name,
    state,
    __time,
    module_id,
    business_key
    -- при наличии в данных можно добавить колонки с точкой (в кавычках):
    -- , "variables.caseId"
    -- , "variables.staticData.caseId"
FROM process_default
ORDER BY __time DESC
LIMIT 50
