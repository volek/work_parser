-- ============================================================================
-- Описание запроса:
-- Файл: `default/q01_select_all.sql`.
-- Стратегия: Default (все поля как отдельные колонки).
-- Модель стратегии: все поля входящего сообщения парсятся и сохраняются в Druid
--   в виде отдельных колонок; имена колонок для переменных — с разделителем точка
--   (например, variables.caseId, variables.staticData.clientEpkId).
-- Типовые таблицы стратегии: default_process_default.
-- Назначение данного запроса: получение детальной выборки по процессам.
--
-- Логика выполнения запроса:
-- 1) Выбор источника данных: default_process_default (общая таблица процессов).
-- 2) Упорядочивание по времени.
-- 3) Ограничение объёма выдачи через LIMIT.
--
-- Колонки верхнего уровня (snake_case): id, process_id, process_name, state,
--   __time, module_id, business_key, node_instances и др.
-- Колонки переменных (с точкой): variables.caseId, variables.staticData.* и т.д.
-- В SQL идентификаторы с точкой указываются в двойных кавычках: "variables.caseId".
-- ============================================================================
SELECT *
FROM default_process_default
ORDER BY __time DESC
LIMIT 100
