-- ============================================================================
-- Описание запроса:
-- Файл: `hybrid/q49_module_performance.sql`.
-- Стратегия: Hybrid (Flat + JSON).
-- Модель стратегии: часто используемые атрибуты вынесены в плоские колонки, вложенные структуры хранятся в JSON.
-- Типовые таблицы стратегии: обычно `process_hybrid`.
-- Назначение данного запроса: получение детальной выборки для анализа.
--
-- Логика выполнения запроса:
-- 1) Выбор источника данных: process_hybrid.
-- 3) Применение фильтров WHERE для отбора релевантных строк.
-- 4) Агрегация данных (GROUP BY и/или агрегатные функции).
-- 6) Упорядочивание результата через ORDER BY.
--
-- Ожидаемые возвращаемые данные и формат:
-- - Тип результата: агрегированный набор (метрики/группы).
-- - Формат строк: одна строка результата на запись/группу согласно SELECT.
-- - Порядок столбцов: соответствует порядку полей в SELECT.
-- - Столбцы результата:
--   - module_id: идентификатор (STRING/UUID/INTEGER по схеме источника).
--   - total: числовой показатель (INTEGER/NUMERIC).
--   - completed: тип определяется выражением в SELECT.
--   - failed: тип определяется выражением в SELECT.
--   - first_seen: тип определяется выражением в SELECT.
--   - last_seen: тип определяется выражением в SELECT.
-- ============================================================================
SELECT 
    module_id,
    COUNT(*) as total,
    SUM(CASE WHEN state = 2 THEN 1 ELSE 0 END) as completed,
    SUM(CASE WHEN state = 3 THEN 1 ELSE 0 END) as failed,
    MIN(__time) as first_seen,
    MAX(__time) as last_seen
FROM process_hybrid
WHERE module_id IS NOT NULL
GROUP BY module_id
ORDER BY total DESC
