-- ============================================================================
-- Описание запроса:
-- Файл: `hybrid/q48_top_clients_count.sql`.
-- Стратегия: Hybrid (Flat + JSON).
-- Модель стратегии: часто используемые атрибуты вынесены в плоские колонки, вложенные структуры хранятся в JSON.
-- Типовые таблицы стратегии: обычно `hybrid_process_hybrid`.
-- Назначение данного запроса: агрегирование и расчёт метрик.
--
-- Логика выполнения запроса:
-- 1) Выбор источника данных: hybrid_process_hybrid.
-- 3) Применение фильтров WHERE для отбора релевантных строк.
-- 4) Агрегация данных (GROUP BY и/или агрегатные функции).
-- 6) Упорядочивание результата через ORDER BY.
-- 7) Ограничение объёма выдачи через LIMIT.
--
-- Ожидаемые возвращаемые данные и формат:
-- - Тип результата: агрегированный набор (метрики/группы).
-- - Формат строк: одна строка результата на запись/группу согласно SELECT.
-- - Порядок столбцов: соответствует порядку полей в SELECT.
-- - Столбцы результата:
--   - var_epkId: идентификатор (STRING/UUID/INTEGER по схеме источника).
--   - var_fio: текст/структура (STRING/JSON/ARRAY).
--   - process_count: числовой показатель (INTEGER/NUMERIC).
--   - distinct_processes: тип определяется выражением в SELECT.
-- ============================================================================
SELECT 
    var_epkId,
    var_fio,
    COUNT(*) as process_count,
    COUNT(DISTINCT process_name) as distinct_processes
FROM hybrid_process_hybrid
WHERE var_epkId IS NOT NULL
GROUP BY var_epkId, var_fio
ORDER BY process_count DESC
LIMIT 20
